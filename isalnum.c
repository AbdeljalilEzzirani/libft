#include "libft.h"
#include <stdio.h>

int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57))
		return (1);
	return (0);
}

int	main ()
{
	int	s;
	int j;

	s = 57;
	j = ft_isalnum(s);
	printf("%d\n", j);
}
