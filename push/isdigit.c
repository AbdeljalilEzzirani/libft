#include "libft.h"
#include <stdio.h>

int	ft_isdigit(int c)
{
	if ((c >= 48 && c <= 57))
		return (1);
	return (0);
}

int	main ()
{
	int	s;
	int j;

	s = 48;
	j = ft_isdigit(s);
	printf("%d\n", j);
}
